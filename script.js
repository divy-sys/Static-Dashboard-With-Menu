


function formdivy() {
    let a = document.getElementById("formsofform").style.display;
    if (a === "none") {
        document.getElementById("formsofform").style.display = "block";
    }
    else if (a === "block") {
        document.getElementById("formsofform").style.display = "none";
    }

}
